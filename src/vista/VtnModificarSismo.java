    /**
     * Modifica un sismo existente
     * @param evt: Evento de presionar el bot√≥n
     */
    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed

          String idString = txtId.getText();
          String idResultado = idString.replaceAll("\\s+","");
          
 
        String e = enumProvincias.getSelectedItem().toString();
        TProvincia nuevoEpicentro = TProvincia.valueOf(e);
        
        if(elControl.esNumero(idResultado)){
            int id = Integer.parseInt(txtId.getText());
            if (rbCoordenadas.isSelected() && elControl.esNumero(txtNuevaLatitud.getText()) 
                    && elControl.esNumero(txtNuevaLongitud.getText())) {
            double nuevaLatitud = Double.parseDouble(txtNuevaLatitud.getText());
            double nuevaLongitud = Double.parseDouble(txtNuevaLongitud.getText());
                if(nuevaLatitud >= 0 && nuevaLongitud <= 0 && elControl.modificarSismo(id, nuevaLatitud, nuevaLongitud)){
                    JOptionPane.showMessageDialog(null, "Latitud y longitud modificadas");
                    dispose();
                }else
                    JOptionPane.showMessageDialog(null, "LATITUD debe ser positiva y LONGITUD negativa ");
            }else if(rbEpicentro.isSelected() && elControl.modificarSismo(id, nuevoEpicentro)) {
                JOptionPane.showMessageDialog(null, "Epicentro modificado");
                dispose();   
            }else
                JOptionPane.showMessageDialog(null, "Seleccione una opci√≥n v√°lida.");        
    }else
        JOptionPane.showMessageDialog(null, "El id debe ser un n√∫mero disponible");
               

    }//GEN-LAST:event_btnModificarActionPerformed

